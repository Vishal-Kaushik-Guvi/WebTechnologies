let grade = 'B';

switch (grade) {
    case 'A':
        console.log("Excellent");
        break;
    case 'B':
        console.log("Very Good");
        break;
    case 'C':
        console.log("Good");
        break;
    case 'D':
        console.log("Fair");
        break;
    case 'F':
        console.log("Fail");
        break;
    default:
        console.log("Invalid grade");
}
